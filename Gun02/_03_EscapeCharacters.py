print("MerhabaDünya")
print("Merhaba\nDünya")  # Yeni satıra geçer
print("Merhaba\tDünya")  # Tab kadar boşluk bırakır
print("Merhaba\bDünya")  # Bir karakter siler (Python'da her zaman beklenen şekilde çalışmayabilir)
print("Merhaba\"Dünya")  # Çift tırnak içerir
print("Merhaba\\Dünya")  # Ters slaş (\) içerir
print("Merhaba\rDünya")  # Satırın başına döner (Python'da her zaman beklenen şekilde çalışmayabilir)
