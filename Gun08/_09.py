# Java kodunun Python'a dönüştürülmüş hali

sayac = 0

sayac = sayac + 1   # sayacın değerini 1 artır: 1
sayac += 1          # sayacın değerini 1 artır: 2
sayac += 1          # sayacın değerini 1 artır: 3

sayac = sayac - 1   # sayacın değerini 1 azaltır: 2
sayac -= 1          # sayacın değerini 1 azaltır: 1
sayac -= 1          # sayacın değerini 1 azaltır: 0


#Java'da olduğu gibi Python'da da sayac++ ve ++sayac gibi özel artırma işlemleri bulunmaz. Bunun yerine Python'da += ve -= operatörleri kullanılarak değer artırma veya azaltma işlemleri gerçekleştirilir.