# 1'den 5'e kadar olan sayıları ekrana yazdırınız.

# While döngüsü kullanılarak
sayac = 1
while sayac <= 5:
    print("sayac while = ", sayac)
    sayac += 1

# For döngüsü kullanılarak
for sayac in range(1, 6):  # 1'den 5'e kadar olan sayıları kapsar
    print("sayac for = ", sayac)

# Döngü Kuralı:
# Bir döngünün kaç kere döneceği belli ise: FOR
# Bir döngünün kaç kere döneceği belli DEĞİL ise: WHILE
# While durumunda kontrol SONDA ise: DO_WHİLE
