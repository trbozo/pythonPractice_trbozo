a = 50
b = 50

print(" a > b", a > b)  # False : a, b'den büyük mü
print(" a >= b", a >= b)  # True : a, b'den büyük eşit mi
print(" a < b", a < b)  # False : a, b'den küçük mü
print(" a <= b", a <= b)  # True : a, b'den küçük eşit mi
print(" a == b", a == b)  # True : a, b'ye eşit mi
print(" a != b", a != b)  # False : a, b'den farklı mı

