sayac = 0

sayac = sayac + 1   # sayacın değerini 1 artırır : 1
sayac += 1          # sayacın değerini 1 artırır : 2
sayac += 1          # sayacın değerini 1 artırır : 3

sayac = sayac - 1   # sayacın değerini 1 azalt : 2
sayac -= 1          # sayacın değerini 1 azalt : 1
sayac -= 1          # sayacın değerini 1 azalt : 0

print(sayac)  # Sonucu görmek için



#Python'da, ++ ve -- operatörleri yoktur. Bunun yerine, += 1 veya -= 1 kullanılır.
